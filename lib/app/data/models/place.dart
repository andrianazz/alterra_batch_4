class Places {
  final String name;
  final String image;
  final List<String> features;
  final String location;
  final String price;
  final String about;
  final double rating;

  Places({
    required this.name,
    required this.image,
    required this.features,
    required this.location,
    required this.price,
    required this.about,
    required this.rating,
  });
}
